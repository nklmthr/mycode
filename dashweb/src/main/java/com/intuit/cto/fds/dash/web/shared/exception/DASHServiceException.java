package com.intuit.cto.fds.dash.web.shared.exception;

public class DASHServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DASHServiceException() {
		super();
	}

	public DASHServiceException(String arg0) {
		super(arg0);
	}

	public DASHServiceException(Throwable arg0) {
		super(arg0);
	}

	public DASHServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
